// W3Schools, C++ Data Types, Basic Data Types
#include <iostream>
#include <string>
using namespace std;

int main() {
    // Creating variables
    int myNum = 5;              // Integer (whole number)
    float myFloatNum = 5.99;    // Floating point number (6-7 decimal digits)
    double myDoubleNum = 9.98;  // Floating point number (15 decimal digits)
    char myLetter = 'D';        // Character
    bool myBoolean = true;      // Boolean
    string myString = "Hello";    // String

    // Print variable values
    cout << "int: " << myNum << "\n";
    cout << "float: " << myFloatNum << "\n";
    cout << "double: " << myDoubleNum << "\n";
    cout << "char: " << myLetter << "\n";
    cout << "bool: " << myBoolean << "\n";
    cout << "string: " << myString;

    return 0;
}
