// W3Schools, C++ Pointers, Create Pointers
#include <iostream>
#include <string>
using namespace std;

int main() {
    string food = "Pizza";  // A food variable of type string
    cout << food << "\n";   // Outputs the value of food (pizza)
    cout << &food << "\n";  // Outputs the memory address of food (0x6fde0)
    return 0;
}
